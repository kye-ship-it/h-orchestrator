/**
 * Build a hierarchical FileNode tree from a flat list of GCS object paths.
 */
function buildTree(paths: string[]): FileNode[] {
  const root: Map<string, FileNode> = new Map();

  for (const filePath of paths) {
    const parts = filePath.split('/');
    let currentLevel = root;
    let currentPath = '';

    for (let i = 0; i < parts.length; i++) {
      const part = parts[i];
      currentPath = currentPath ? `${currentPath}/${part}` : part;
      const isFile = i === parts.length - 1;

      if (!currentLevel.has(part)) {
        const node: FileNode = {
          name: part,
          path: isFile ? currentPath : `${currentPath}/`,
          type: isFile ? 'file' : 'folder',
          children: isFile ? undefined : [],
        };
        currentLevel.set(part, node);
      }

      const existing = currentLevel.get(part)!;
      if (!isFile) {
        // Navigate into children for the next level
        const childMap = new Map<string, FileNode>();
        if (existing.children) {
          for (const child of existing.children) {
            childMap.set(child.name, child);
          }
        }
        currentLevel = childMap;
        // Sync childMap back to existing.children after processing
        // We need a reference-based approach instead
      }
    }
  }

  // Re-implement with a simpler recursive insert
  return buildTreeSimple(paths);
}

function buildTreeSimple(paths: string[]): FileNode[] {
  interface TreeMap {
    [key: string]: TreeMap | null; // null = leaf (file)
  }

  const treeMap: TreeMap = {};

  for (const filePath of paths) {
    const parts = filePath.split('/');
    let current: TreeMap = treeMap;
    for (let i = 0; i < parts.length; i++) {
      const part = parts[i];
      const isLast = i === parts.length - 1;
      if (isLast) {
        current[part] = null;
      } else {
        if (current[part] === null || current[part] === undefined) {
          current[part] = {};
        }
        current = current[part] as TreeMap;
      }
    }
  }

  function mapToNodes(map: TreeMap, parentPath: string): FileNode[] {
    const nodes: FileNode[] = [];
    const entries = Object.entries(map).sort(([a], [b]) => a.localeCompare(b));

    for (const [name, value] of entries) {
      const fullPath = parentPath ? `${parentPath}/${name}` : name;
      if (value === null) {
        nodes.push({ name, path: fullPath, type: 'file' });
      } else {
        nodes.push({
          name,
          path: `${fullPath}/`,
          type: 'folder',
          children: mapToNodes(value, fullPath),
        });
      }
    }
    return nodes;
  }

  return mapToNodes(treeMap, '');
}

/**
 * Score a file against a search query. Returns null if no match.
 */
function scoreFile(
  filePath: string,
  content: string,
  queryLower: string
): SearchResult | null {
  let score = 0;
  let frontmatterRecord: Record<string, string> = {};
  let body = content;
  let title = '';

  try {
    const parsed = matter(content);
    body = parsed.content;
    frontmatterRecord = Object.fromEntries(
      Object.entries(parsed.data).map(([k, v]) => [k, String(v)])
    );

    // Check frontmatter fields for matches
    for (const value of Object.values(frontmatterRecord)) {
      if (String(value).toLowerCase().includes(queryLower)) {
        score += 10;
      }
    }
  } catch {
    // If frontmatter parsing fails, search raw content
  }

  // Extract first H1 as title
  const titleMatch = body.match(/^#\s+(.+)$/m);
  if (titleMatch) {
    title = titleMatch[1].trim();
    if (title.toLowerCase().includes(queryLower)) {
      score += 5;
    }
  }

  // Body text match
  if (body.toLowerCase().includes(queryLower)) {
    score += 1;
  }

  if (score === 0) return null;

  // Extract snippet around the first match in body
  const snippet = extractSnippet(body, queryLower);

  return {
    path: filePath,
    title: title || filePath.split('/').pop()?.replace('.md', '') || filePath,
    snippet,
    frontmatter: frontmatterRecord,
    score,
  };
}

/**
 * Extract a short snippet of text around the first occurrence of the query.
 */
function extractSnippet(text: string, queryLower: string): string {
  const SNIPPET_RADIUS = 80;
  const plainText = text.replace(/[#*_`~\[\]()>]/g, '').trim();
  const idx = plainText.toLowerCase().indexOf(queryLower);

  if (idx === -1) {
    return plainText.slice(0, SNIPPET_RADIUS * 2) + (plainText.length > SNIPPET_RADIUS * 2 ? '...' : '');
  }

  const start = Math.max(0, idx - SNIPPET_RADIUS);
  const end = Math.min(plainText.length, idx + queryLower.length + SNIPPET_RADIUS);
  const prefix = start > 0 ? '...' : '';
  const suffix = end < plainText.length ? '...' : '';

  return `${prefix}${plainText.slice(start, end)}${suffix}`;
}
