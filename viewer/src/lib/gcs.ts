/**
 * List all files in the bucket as a hierarchical tree.
 */
export async function listFileTree(prefix?: string): Promise<FileNode[]> {
  const bucket = getBucket();
  if (!bucket) return MOCK_TREE;

  try {
    const [files] = await bucket.getFiles({ prefix: prefix ?? '' });
    const tree = buildTree(
      files
        .map((f) => f.name)
        .filter((name) => !name.endsWith('/')) // skip directory markers
    );
    return tree;
  } catch (error) {
    console.error('GCS listFileTree error:', error);
    return [];
  }
}

/**
 * Read a single markdown file from the bucket.
 */
export async function readFile(path: string): Promise<string | null> {
  const bucket = getBucket();
  if (!bucket) return MOCK_FILES[path] ?? null;

  try {
    const file = bucket.file(path);
    const [exists] = await file.exists();
    if (!exists) return null;

    const [buffer] = await file.download();
    return buffer.toString('utf-8');
  } catch (error) {
    console.error(`GCS readFile error for "${path}":`, error);
    return null;
  }
}

/**
 * Search across all markdown files.
 *
 * Supports two modes:
 * - `"semantic"` (default): uses Vertex AI embeddings + cosine similarity
 *   against the pre-built index at `index/embeddings.json`. Falls back to
 *   keyword search when the index is unavailable.
 * - `"keyword"`: traditional frontmatter + body text matching.
 *
 * Returns `{ results, mode }` where `mode` indicates which strategy was
 * actually used. The function signature stays backward-compatible.
 */
export async function searchFiles(
  query: string,
  prefix?: string,
  mode: 'semantic' | 'keyword' = 'semantic'
): Promise<SearchResult[]> {
  // Mock mode always uses keyword search
  const bucket = getBucket();
  if (!bucket) {
    return keywordSearch(query, prefix);
  }

  // Try semantic search when requested
  if (mode === 'semantic') {
    try {
      const results = await semanticSearch(query);
      if (results !== null) {
        return results;
      }
      // Index unavailable — fall through to keyword search
    } catch (error) {
      console.error('Semantic search failed, falling back to keyword:', error);
    }
  }

  return keywordSearchGCS(query, prefix, bucket);
}

/**
 * Indicates which search mode will actually be used (for UI display).
 */
export async function resolveSearchMode(
  requested: 'semantic' | 'keyword'
): Promise<'semantic' | 'keyword'> {
  if (requested === 'keyword') return 'keyword';

  const bucket = getBucket();
  if (!bucket) return 'keyword';

  try {
    const { isIndexAvailable } = await import('./embeddings');
    const available = await isIndexAvailable();
    return available ? 'semantic' : 'keyword';
  } catch {
    return 'keyword';
  }
}

/**
 * Keyword search against mock data.
 */
function keywordSearch(query: string, _prefix?: string): SearchResult[] {
  const q = query.toLowerCase();
  const results: SearchResult[] = [];
  for (const [filePath, content] of Object.entries(MOCK_FILES)) {
    const result = scoreFile(filePath, content, q);
    if (result) results.push(result);
  }
  results.sort((a, b) => b.score - a.score);
  return results;
}

/**
 * Keyword search against live GCS bucket.
 */
async function keywordSearchGCS(
  query: string,
  prefix: string | undefined,
  bucket: NonNullable<ReturnType<typeof getBucket>>
): Promise<SearchResult[]> {
  try {
    const [files] = await bucket.getFiles({
      prefix: prefix ?? '',
    });
    const mdFiles = files.filter((f) => f.name.endsWith('.md'));

    const queryLower = query.toLowerCase();
    const results: SearchResult[] = [];

    // Process files concurrently in batches to avoid overwhelming memory
    const BATCH_SIZE = 50;
    for (let i = 0; i < mdFiles.length; i += BATCH_SIZE) {
      const batch = mdFiles.slice(i, i + BATCH_SIZE);
      const batchResults = await Promise.allSettled(
        batch.map(async (file) => {
          const [buffer] = await file.download();
          const content = buffer.toString('utf-8');
          return scoreFile(file.name, content, queryLower);
        })
      );

      for (const result of batchResults) {
        if (result.status === 'fulfilled' && result.value) {
          results.push(result.value);
        }
      }
    }

    results.sort((a, b) => b.score - a.score);
    return results;
  } catch (error) {
    console.error('GCS keywordSearchGCS error:', error);
    return [];
  }
}

/**
 * Get the most recent daily log by lexicographic sorting of file names
 * under the daily-logs/ prefix.
 */
export async function getLatestDailyLog(): Promise<{
  path: string;
  content: string;
} | null> {
  const bucket = getBucket();

  if (!bucket) {
    return {
      path: 'daily/h-voice/2026/03/2026-03-25.md',
      content: MOCK_FILES['daily/h-voice/2026/03/2026-03-25.md'],
    };
  }

  try {
    const [files] = await bucket.getFiles({ prefix: 'daily-logs/' });
    const mdFiles = files
      .filter((f) => f.name.endsWith('.md'))
      .sort((a, b) => b.name.localeCompare(a.name));

    if (mdFiles.length === 0) return null;

    const latest = mdFiles[0];
    const [buffer] = await latest.download();
    return {
      path: latest.name,
      content: buffer.toString('utf-8'),
    };
  } catch (error) {
    console.error('GCS getLatestDailyLog error:', error);
    return null;
  }
}

/**
 * Upload a file to the bucket and return its GCS path.
 */
export async function writeFile(
  path: string,
  content: string
): Promise<string> {
  const bucket = getBucket();
  if (!bucket) {
    console.warn('GCS not configured. Skipping write for:', path);
    return path;
  }

  const file = bucket.file(path);
  await file.save(content, {
    contentType: 'text/markdown',
    metadata: { cacheControl: 'no-cache' },
  });
  return path;
}

